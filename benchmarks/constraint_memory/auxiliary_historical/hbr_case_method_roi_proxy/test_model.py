import math

def calculate_roi_impact(initial_project_failure_rate, project_value, project_cost, reduction_in_failure_rate, WACC, forecast_period_months):
    """
    Calculates the impact on a simplified ROI metric (Expected NPV / Cost) based on a reduction in project failure rate.
    
    Args:
        initial_project_failure_rate (float): Baseline failure rate of projects (0 to 1).
        project_value (float): Expected value generated by a successful project.
        project_cost (float): Initial cost of the project.
        reduction_in_failure_rate (float): Absolute reduction in failure rate due to intervention (0 to 1).
        WACC (float): Weighted Average Cost of Capital (annual, 0 to 1).
        forecast_period_months (int): Period in months over which to calculate NPV.

    Returns:
        float: The absolute increase in the ROI metric (new_roi_proxy - original_roi_proxy).
    """
    # Type and value validation
    if not all(isinstance(arg, (int, float)) for arg in [initial_project_failure_rate, project_value, project_cost, reduction_in_failure_rate, WACC, forecast_period_months]):
        raise TypeError("All arguments must be numeric.")
    if not (0 <= initial_project_failure_rate <= 1 and 0 <= reduction_in_failure_rate <= 1):
        raise ValueError("Failure rates must be between 0 and 1.")
    if WACC <= 0:
        raise ValueError("WACC must be positive for discounting.")
    if project_value <= 0 or project_cost <= 0:
        raise ValueError("Project value and cost must be positive.")
    if forecast_period_months <= 0:
        raise ValueError("Forecast period must be positive.")
    if initial_project_failure_rate - reduction_in_failure_rate < 0:
        raise ValueError("Reduced failure rate cannot be negative.")

    # Convert annual WACC to monthly discount rate
    monthly_discount_rate = (1 + WACC)**(1/12) - 1
    
    # Discount factor for the forecast period
    discount_factor = 1 / (1 + monthly_discount_rate)**forecast_period_months
    
    # Net Present Value (NPV) of a successful project (ignoring cash flow distribution for simplicity)
    # Assumes (project_value - project_cost) represents the net cash flow if successful, discounted once.
    npv_successful_project_cashflow = (project_value - project_cost) 
    npv_successful_project = npv_successful_project_cashflow * discount_factor
    
    # Original Scenario
    original_success_rate = 1 - initial_project_failure_rate
    original_expected_npv_per_project = (original_success_rate * npv_successful_project) - \
                                        (initial_project_failure_rate * project_cost * discount_factor)
    original_roi_proxy = original_expected_npv_per_project / project_cost

    # New Scenario with reduced failure rate
    new_failure_rate = initial_project_failure_rate - reduction_in_failure_rate
    new_success_rate = 1 - new_failure_rate
    new_expected_npv_per_project = (new_success_rate * npv_successful_project) - \
                                   (new_failure_rate * project_cost * discount_factor)
    new_roi_proxy = new_expected_npv_per_project / project_cost
    
    roi_increase = new_roi_proxy - original_roi_proxy
    
    return roi_increase

# --- LOAD-BEARING VARIABLES (from markdown table) ---
AVG_STRATEGIC_PLANNING_PROJECT_COST = 250000.0  # USD
PROJECT_FAILURE_RATE_REDUCTION_TARGET = 0.05    # 5 percentage points
WACC = 0.10                                     # 10%
AVERAGE_PROJECT_VALUE_IF_SUCCESSFUL = 1500000.0 # USD
BASELINE_PROJECT_FAILURE_RATE_AI_DRIVEN = 0.20  # 20%
FORECAST_PERIOD_MONTHS_ROI_PREDICTION = 24      # 24 months
MIN_EXPECTED_ROI_INCREASE_PERCENTAGE_POINTS = 0.02 # 2 percentage points absolute increase

# --- EXECUTE CALCULATION AND ASSERTION ---
calculated_roi_increase = calculate_roi_impact(
    initial_project_failure_rate=BASELINE_PROJECT_FAILURE_RATE_AI_DRIVEN,
    project_value=AVERAGE_PROJECT_VALUE_IF_SUCCESSFUL,
    project_cost=AVG_STRATEGIC_PLANNING_PROJECT_COST,
    reduction_in_failure_rate=PROJECT_FAILURE_RATE_REDUCTION_TARGET,
    WACC=WACC,
    forecast_period_months=FORECAST_PERIOD_MONTHS_ROI_PREDICTION
)

assert calculated_roi_increase >= MIN_EXPECTED_ROI_INCREASE_PERCENTAGE_POINTS, \
    f"Predicted ROI increase ({calculated_roi_increase:.4f} absolute percentage points) is less than the target " \
    f"{MIN_EXPECTED_ROI_INCREASE_PERCENTAGE_POINTS:.2f} absolute percentage points."

print(f"Calculated ROI increase (absolute percentage points): {calculated_roi_increase:.4f}")
print(f"Assertion passed: ROI increase is at least {MIN_EXPECTED_ROI_INCREASE_PERCENTAGE_POINTS:.2f} percentage points.")
